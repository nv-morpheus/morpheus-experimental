import json
import os
import pickle

import numpy as np

with open('../dataset/azure-ad-logs-sample-training-data.json', 'r') as file:
    data = json.load(file)

json_strings = [json.dumps(dictionary) for dictionary in data]
combined_json_string = '\n'.join(json_strings)

data = combined_json_string

# get all the unique characters that occur in this text
chars = sorted(list(set(data)))
vocab_size = len(chars)
print("all the unique characters:", ''.join(chars))
print(f"vocab size: {vocab_size:,}")

# create a mapping from characters to integers
stoi = {ch: i for i, ch in enumerate(chars)}
itos = {i: ch for i, ch in enumerate(chars)}


def encode(s):
    return [stoi[c] for c in s]  # encoder: take a string, output a list of integers


def decode(sequence):
    return ''.join([itos[i] for i in sequence])  # decoder: take a list of integers, output a string


# create the train and test splits
n = len(data)
train_data = data[:int(n * 0.9)]
val_data = data[int(n * 0.9):]

# encode both to integers
train_ids = encode(train_data)
val_ids = encode(val_data)
print(f"train has {len(train_ids):,} tokens")
print(f"val has {len(val_ids):,} tokens")

# export to bin files
train_ids = np.array(train_ids, dtype=np.uint16)
val_ids = np.array(val_ids, dtype=np.uint16)
train_ids.tofile(os.path.join(os.path.dirname(__file__), '../model/train.bin'))
val_ids.tofile(os.path.join(os.path.dirname(__file__), '../model/val.bin'))

# save the meta information as well, to help us encode/decode later
meta = {
    'vocab_size': vocab_size,
    'itos': itos,
    'stoi': stoi,
}
with open(os.path.join(os.path.dirname(__file__), '../model/meta.pkl'), 'wb') as f:
    pickle.dump(meta, f)
